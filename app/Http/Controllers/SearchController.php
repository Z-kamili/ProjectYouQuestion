     */
    public function edit($id)
    {
        $data = DB::table('questions')->join('users','users.id', '=' ,'questions.ID_user')->join('options','options.id','=','questions.ID_opt')->select('questions.titre','questions.id','questions.Desc as Description','questions.created_at','users.name')->where('options.Desc',$id)->paginate(4);
        $categorie = Option::all();
        return view('search.search',[
            'Data'=> $data,
            'Categories' => $categorie,
        ]);
    }

